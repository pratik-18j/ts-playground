/* Important Terminologies
 1. Type or Data Type:
 2. Annotation:
 3. Inference:
 4. Type Assertion:

Example:
let age: number = 25; 
Here, 
'number' => data type
'number' => annotation (Explicitly applying the data type to the variable 'age')

let age= 25;
Type Inference: TypeScript can automatically infer the type of a variable based on the value assigned to it.'age' => variable name. The value 25 is assigned to the variable 'age'.

Type Assertion: Type assertion is a way to tell the TypeScript compiler about the type of a variable when you have more information about the variable's type than TypeScript can infer. It allows you to override the inferred type and specify a different type for the variable.
Example:
let age: any = 25;

 */



